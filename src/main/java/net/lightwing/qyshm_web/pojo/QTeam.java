package net.lightwing.qyshm_web.pojo;


public class QTeam {

  private long tid;
  private long ttypeid;
  private String theadimg;
  private String name;
  private String tdetail;
  private long status;


  public long getTid() {
    return tid;
  }

  public void setTid(long tid) {
    this.tid = tid;
  }


  public long getTtypeid() {
    return ttypeid;
  }

  public void setTtypeid(long ttypeid) {
    this.ttypeid = ttypeid;
  }


  public String getTheadimg() {
    return theadimg;
  }

  public void setTheadimg(String theadimg) {
    this.theadimg = theadimg;
  }


  public String getName() {
    return name;
  }

  public void setName(String name) {
    this.name = name;
  }


  public String getTdetail() {
    return tdetail;
  }

  public void setTdetail(String tdetail) {
    this.tdetail = tdetail;
  }


  public long getStatus() {
    return status;
  }

  public void setStatus(long status) {
    this.status = status;
  }

}
